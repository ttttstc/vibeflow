#!/usr/bin/env python3
"""
Initialize a vibeflow project structure.

Creates the deterministic scaffold artifacts needed for multi-session agent work:
- feature-list.json (empty template with tech_stack, quality_gates, required_configs)
- .vibeflow/state.json (centralized workflow state)
- .vibeflow/logs/session-log.md (human-readable progress log, generated lazily)
- docs/overview/ (global project context for new contributors)
- RELEASE_NOTES.md (living release notes, Keep a Changelog format)
- CLAUDE.md (appended with vibeflow reference for cross-session continuity)
- examples/ directory with README.md (runnable examples for completed features)
- scripts/ directory (for validation and helper scripts)
- docs/changes/<change-id>/ directory (for work-package artifacts)

NOTE: The following files are NOT generated by this script — they are created by the
LLM during the Initializer phase, tailored to the project's specific tech stack:
- .vibeflow/guides/build.md (Worker session guide)
- init.sh / init.ps1 (environment bootstrap — LLM generates from design doc)
"""

import argparse
import json
import os
import shutil
import sys
from datetime import datetime
from pathlib import Path


SCRIPT_DIR = Path(__file__).resolve().parent
if str(SCRIPT_DIR) not in sys.path:
    sys.path.insert(0, str(SCRIPT_DIR))

from vibeflow_overview import ensure_overview_docs  # noqa: E402
from vibeflow_paths import default_state, save_state  # noqa: E402


CLAUDE_MD_MARKER = "<!-- vibeflow -->"

_VIBEFLOW_REFERENCE_BODY = (
    "\n\n<!-- vibeflow -->\n"
    "## VibeFlow\n\n"
    "This project uses a multi-session agent workflow.\n"
    "Flow: Spark → Design → Tasks → Build → Review → Test → Ship → Reflect.\n\n"
    "Incremental development: requests live under `.vibeflow/increments/`, active work packages live under `docs/changes/<change-id>/`.\n\n"
    "Key files: `docs/overview/CURRENT-STATE.md` (project snapshot), "
    "`docs/overview/ARCHITECTURE.md` (global architecture), "
    "`docs/changes/<change-id>/brief.md` (goal, scope, and constraints), "
    "`docs/changes/<change-id>/design.md` (design), "
    "`docs/changes/<change-id>/tasks.md` (execution handoff), "
    "`feature-list.json` (task inventory), "
    "`.vibeflow/state.json` (workflow state and runtime snapshot), "
    "`RELEASE_NOTES.md` (changelog), "
    "`docs/test-cases/feature-*.md` (per-feature ST test cases), "
    "`.vibeflow/increments/requests/*.json` (increment requests).\n"
    "<!-- /vibeflow -->\n"
)

CLAUDE_MD_REFERENCE = _VIBEFLOW_REFERENCE_BODY


def _append_agent_md_reference(out_dir: str, project_name: str, filename: str):
    """Append vibeflow reference to an agent instruction file (idempotent)."""
    path = os.path.join(out_dir, filename)

    if os.path.exists(path):
        with open(path, "r", encoding="utf-8") as f:
            content = f.read()
        if CLAUDE_MD_MARKER in content:
            print(f"Skipped: {path} (reference already exists)")
            return
        with open(path, "a", encoding="utf-8") as f:
            f.write(_VIBEFLOW_REFERENCE_BODY)
        print(f"Updated: {path} (appended vibeflow reference)")
    else:
        with open(path, "w", encoding="utf-8") as f:
            f.write(f"# {project_name}\n")
            f.write(_VIBEFLOW_REFERENCE_BODY)
        print(f"Created: {path}")


def append_claude_md_reference(out_dir: str, project_name: str):
    """Append vibeflow reference to CLAUDE.md (idempotent)."""
    _append_agent_md_reference(out_dir, project_name, "CLAUDE.md")



def create_feature_list(
    project_name: str,
    language: str = "TODO",
    test_framework: str = "TODO",
    coverage_tool: str = "TODO",
    mutation_tool: str = "TODO",
    line_coverage_min: int = 90,
    branch_coverage_min: int = 80,
    mutation_score_min: int = 80,
) -> dict:
    return {
        "project": project_name,
        "created": datetime.now().strftime("%Y-%m-%d"),
        "tech_stack": {
            "language": language,
            "test_framework": test_framework,
            "coverage_tool": coverage_tool,
            "mutation_tool": mutation_tool
        },
        "quality_gates": {
            "line_coverage_min": line_coverage_min,
            "branch_coverage_min": branch_coverage_min,
            "mutation_score_min": mutation_score_min
        },
        "constraints": [],
        "assumptions": [],
        "required_configs": [],
        "features": []
    }


